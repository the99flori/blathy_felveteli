Benyújtott SNI, BTMN kedvezmény kérelemre a rendelkezésre álló idő meghosszabbodhat, és ezért a matematika írásbeli kezdete módosulhat. A kérelmet benyújtók erről az elektronikus rendszeren keresztül tölthetik le a határozatot.
A rajzok készítésén kívül minden írásbeli munkát kék tollal kell végezni!

A magyar nyelvi feladatlapok kitöltéséhez segédeszköz nem használható.
A matematika feladatlapok kitöltéséhez a fent felsorolt rajzeszközökön kívül egyéb segédeszköz nem használható.

A különleges helyzetű (SNI, BTMN) tanulók által használható segédeszközök köre igazgatói határozat alapján a fentieknél bővebb lehet. Az esetleges szülői kérelmen és szakértői véleményen alapuló kedvezményekről az elektronikus rendszeren keresztül tölthetik le a határozatot.

Az egyéni terembeosztásról a felvételi napján az iskola aulájában kifüggesztett hirdetésről vagy a honlapon keresztül elérhető személyre szabott értesítőből tájékozódhatnak.

A kiértékelt írásbeli dolgozatokat 2022. január 28-án 8:00-16:00-ig lehet megtekinteni az iskolában. A szülők és gondviselők az iskola épületébe az iskola hatályos eljárásrendje ("Eljárásrend az oktatás megszervezésére egészségügyi válsághelyzet és járványügyi készültség, valamint veszélyhelyzet esetére (V 4.2)") alapján csak a megfelelő védettségi igazolás felmutatásával léphetnek be. A megtekintés helyszíne az első emeleten található színházterem. A dolgozatokról elektronikus másolatot (fénykép) készíthet a felvételiző vagy szülő. A dolgozat javításával kapcsolatban írásbeli észrevétel tehető (kizárólag a hivatalos javítási értékelési útmutatótól való eltérés esetén) 2022. január 31-én 16:00-ig. Az észrevétel benyújtására nyitva álló határidő elmulasztása esetén egy napon belül lehet igazolási kérelmet benyújtani. Az igazolási kérelem benyújtási határideje jogvesztő.

A központi írásbeli vizsga eredményéről a részletes és hiteles értékelő lapot a felvételit szervező középiskolában a megtekintés időpontjában vagy 2022. január 31-én 8 és 14.30 között veheti át a felvételiző vagy a szülő, akadályoztatás esetén a kéttanús, írásos meghatalmazással rendelkező megbízott. Az írásbeli észrevételt benyújtó felvételizőknek (szülőknek) 2022. február 7-ig adjuk át az értékelő lapot.

A központi felvételi vizsgához minden vizsgázónak sok sikert kívánok!

