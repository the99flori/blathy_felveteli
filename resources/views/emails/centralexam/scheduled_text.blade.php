Központi írásbeli felvételi - Vizsgabehívó és tájékoztató

Jelen levél egy általános tájékoztató a felvételizők részére, a személyre szabott (vizsgaterem számát, benyújtott kérelem alapján engedélyezett kedvezményeket, stb. tartalmazó) személyre szabott vizsgaértesítőt elérhetik az iskola honlapján (https://felveteli.blathy.info). Az értesítő megtekintéséhez az oktatási azonosító számot (7-el kezdődő 11 jegyű szám) és a születési dátumot (ÉÉÉÉ. HH. NN. formátumban, pl.: 2006. 02. 03.) kell megadni.

Értesítem, hogy Önök jelentkeztek a központi középiskolai (9. évfolyamra) írásbeli felvételi vizsgára magyar nyelv és matematika tantárgyakból.

A központi írásbeli felvételi vizsga dátuma: 2022. január 22. szombat. Kérjük, hogy a felvételi napján az iskola épületébe csak a felvételizők lépjenek be! A kísérők a felvételizőket csak az iskola bejáratáig (porta) kísérjék.

A központi írásbeli felvételire a BMSZC Bláthy-ba jelentkezett felvételiző a megadott napon legkésőbb 9:30-ig érkezzen be az iskolánkba 1032 Budapest, Bécsi út 134. és a következőket hozza magával:
